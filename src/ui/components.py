def translate_progress(status: str, language: str = "English") -> str:
    """Translate a backend progress status string to the selected language."""
    if language == "English" or language not in _PROGRESS:
        return status
    import re
    lang = _PROGRESS[language]
    if status in lang["fixed"]:
        return lang["fixed"][status]
    for pattern, template in lang["patterns"]:
        m = re.match(pattern, status)
        if m:
            return template.format(*m.groups())
    return status


def format_task_recommendation(task: dict, language: str = "English") -> str:
    """Format a task-level staffing recommendation."""
    lb = get_labels(language)
    resources = task.get("resources", [])
    _rec_map = {"Primary": lb["primary"], "Support": lb["support"]}
    resource_lines = []
    for r in resources:
        skills = r.get("matched_skills", [])
        skills_str = (", ".join(skills[:4]) if skills else "")
        skills_part = f" · {skills_str}" if skills_str else ""
        rec_label = _rec_map.get(r.get("recommendation", ""), r.get("recommendation", "Recommended"))
        resource_lines.append(
            f"{rec_label}: "
            f"**{r.get('full_name', 'N/A')}** "
            f"({lb['fit_score'].lower()} {r.get('fit_score', 0):.2f}, "
            f"{r.get('availability', 0)} {lb['hrs_wk']})"
            f"{skills_part}"
        )
    if not resource_lines:
        resource_lines = [lb["needs_staffing"]]
    skills = ", ".join(task.get("required_skills", [])) or "N/A"
    return (
        f"**{lb['task']} {task.get('task_id', '?')}: "
        f"{task.get('task', 'N/A')}**  \n"
        f"{lb['skills']}: `{skills}` | {lb['strength']}: "
        f"**{task.get('strength_score', 0):.2f}**  \n"
        + "  \n".join(resource_lines)
    )
