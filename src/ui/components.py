def format_resource_card(resource: dict, language: str = "English") -> str:
    """Format a resource recommendation for display."""
    lb = get_labels(language)
    skills = resource.get("matched_skills", [])
    skills_str = (", ".join(skills[:4]) if skills else "")
    skills_part = f"  \n{lb['skills']}: {skills_str}" if skills_str else ""
    return (
        f"**{resource.get('full_name', 'N/A')}** "
        f"[{resource.get('priority', 'N/A')}]  \n"
        f"{lb['fit_score']}: **{resource.get('fit_score', 0):.2f}** "
        f"| {lb['availability']}: {resource.get('availability', 0)} {lb['hrs_wk']}"
        f"{skills_part}"
    )


